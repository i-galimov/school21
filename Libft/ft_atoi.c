#include "libft.h"

int	ft_atoi(const char *s)
{
	int		neg;
	long	num;
	int		i;

	num = 0;
	neg = 1;
	i = 0;
	while (s[i] == ' ' || s[i] == '\f' || s[i] == '\n' ||s[i] == '\r' || s[i] == '\t' || s[i] == '\v' || s[i] == '+')
		i++;
	if (s[i] == '-')
		neg *= -1;
		i++;
	while (s[i] >= '0' && s[i] <= '9')
		num = num * 10 + s[i] - '0';
		i++;
	return (neg * (int)num);
}